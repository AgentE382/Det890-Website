<h2>TSgt Tinisha L. Bryant: NCOIC</h2>

<p>TSgt Tinisha L. Bryant is the Non-Commissioned Officer in Charge (NCOIC) of AFROTC Detachment 890, Charlottesville, VA.  She is responsible for the developing and mentoring junior NCO cadre and cadets.</p>

<p>TSgt Bryant grew up in Portsmouth, Virginia and entered the Air Force in May 1998. She has served as Non-Commissioned Officer in Charge for three Commander Support Staffs (CSS). She has been stationed at locations in Virginia, Germany, Florida and Georgia to include a deployment to Saudi Arabia and CENTCOM. Before assuming her current position, TSgt Bryant served as Non-Commissioned Officer in Charge, 15th Air Support Operations Squadron (ASOS) at Fort Stewart Army Installation, Hinesville, GA where she was responsible for 150 personnel.</p>

