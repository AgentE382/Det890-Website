<h2>Colonel Steven T. Hiss: Commander</h2>

<p>Col Steven T. Hiss is Commander, Air Force Reserve Officer Training Corps Detachment 890 at the University of Virginia in Charlottesville. He commands a detachment of seven cadre and 150 cadets located at the University of Virginia, Liberty University, James Madison University, and Piedmont Virginia Community College.</p>

<p>Col Hiss was commissioned into the U.S. Air Force in 1986 as a distinguished graduate of Purdue University's ROTC program. Following pilot training, he served as a T-38 Instructor Pilot and Wing Executive Officer at Columbus AFB, Miss.  He transitioned to the B-1B in 1991, serving operational tours at Grand Forks AFB, N.D., Ellsworth AFB, S.D., Mt. Home AFB, Idaho, and Dyess AFB, Texas. He also served as aide-de-camp to the Commander-in-Chief, U.S. Joint Forces Command and Supreme Allied Commander, Atlantic. Col Hiss commanded the 436th Training Squadron at Dyess AFB and served as a Deputy Division Chief and Division Chief on the Air Staff. Most recently, he served as Vice Commander of the 28th Bomb Wing at Ellsworth AFB, S.D., the largest operational B-1B wing in the Air Force. He has deployed numerous times in support of Operations Southern Watch, Iraqi Freedom, and Enduring Freedom.</p>

<p>Col Hiss is command pilot with more than 3,700 flight hours in the B-1B and T-38.</p>

