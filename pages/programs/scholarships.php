<h2>Scholarships</h2>

<p>There are many opportunities available to students who are interested in pursuing a scholarship through the Air Force, 
both while in high school and in college. More information can be found at 
<a href="http://www.afrotc.com/scholarships/">http://www.afrotc.com/scholarships/</a>. 
This page will help prospective cadets navigate through the process of potentially earning scholarships and answer any 
questions they may have.  If you are interested in joining Air Force ROTC at Detachment 890 or would like more information 
on how to join, please contact the Detachment by phone, by e-mail, or in person.</p>

