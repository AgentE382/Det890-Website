<h2>Air Force 101</h2>

<p>
	Future Air Force Leaders,
</p>
<p>
 
We'd like to invite you to attend our orientation program, Air Force 101, which is held before the start of every fall semester.  This is a 48-hour crash course to Air Force ROTC.  You'll meet other first year students and will be able to move into your dorm before the rest of the freshman class. By attending AF101, you will be given a taste of why the cadet staff decided to become officers in the Air Force through the ROTC program! Air Force 101 will give you the opportunity to learn how to work as a team, improve your physical fitness, and ultimately learn about yourself. It will be fun and motivational, but also challenging. You'll be glad that you came. AF101 is one of the best experiences of your cadet life, so you donâ€™t want to miss out!
