<h2>Project Global Officer</h2>

<p> An initiative from the Defense Language and National Security Education Office, Project GO is meant to expose aspiring and current 
military personnel to foreign language and culture training. Opportunities range from intense study within designated universities, to
introductory summer courses that practice language immersion. Whether you're a foreign language expert or haven't ever studied before,
Project GO could be the perfect match for you. Go to <a href="http://www.rotcprojectgo.org">http://www.rotcprojectgo.org</a>
for more information.
</p>

